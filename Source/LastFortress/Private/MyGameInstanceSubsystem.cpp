void UMyGameInstanceSubsystem::OnFindSessionsCompleted(bool Successful)
{
	const IOnlineSessionPtr sessionInterface = Online::GetSessionInterface(GetWorld());
	if (sessionInterface)
	{
		sessionInterface->ClearOnFindSessionsCompleteDelegate_Handle(FindSessionsCompleteDelegateHandle);
	}


	if (LastSessionSearch->SearchResults.Num() <= 0)
	{
		OnFindSessionsCompleteEvent.Broadcast(TArray<FMySessionSearchResult>(), Successful);
		return;
	}
	TArray<FMySessionSearchResult> Res;
	for (size_t i = 0; i < LastSessionSearch->SearchResults.Num(); i++)
	{
		Res.Add(FMySessionSearchResult(LastSessionSearch->SearchResults[i], "", ""));
	}

	OnFindSessionsCompleteEvent.Broadcast(Res, Successful);
}

