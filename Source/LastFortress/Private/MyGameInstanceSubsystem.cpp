#include "OnlineSubsystemUtils.h"

#include "MyGameInstanceSubsystem.h"

UMyGameInstanceSubsystem::UMyGameInstanceSubsystem()
{
	CreateSessionCompleteDelegate.BindUObject(this, &ThisClass::OnCreateSessionCompleted);
	StartSessionCompleteDelegate.BindUObject(this, &ThisClass::OnStartSessionCompleted);
	EndSessionCompleteDelegate.BindUObject(this, &ThisClass::OnEndSessionCompleted);
	DestroySessionCompleteDelegate.BindUObject(this, &ThisClass::OnDestroySessionCompleted);
	FindSessionsCompleteDelegate.BindUObject(this, &ThisClass::OnFindSessionsCompleted);
	JoinSessionCompleteDelegate.BindUObject(this, &ThisClass::OnJoinSessionCompleted);


}

void UMyGameInstanceSubsystem::CreateSession(int32 NumPublicConnections, bool IsLANMatch,FString MapName)
{
	const IOnlineSessionPtr sessionInterface = Online::GetSessionInterface(GetWorld());
	if (!sessionInterface.IsValid())
	{
		OnCreateSessionCompleteEvent.Broadcast(false);
		return;
	}

	LastSessionSettings = MakeShareable(new FOnlineSessionSettings());
	LastSessionSettings->NumPrivateConnections = 0;
	LastSessionSettings->NumPublicConnections = NumPublicConnections;
	LastSessionSettings->bAllowInvites = true;
	LastSessionSettings->bAllowJoinInProgress = true;
	LastSessionSettings->bAllowJoinViaPresence = true;
	LastSessionSettings->bAllowJoinViaPresenceFriendsOnly = true;
	LastSessionSettings->bIsDedicated = false;
	LastSessionSettings->bUsesPresence = true;
	LastSessionSettings->bIsLANMatch = IsLANMatch;
	LastSessionSettings->bShouldAdvertise = true;
	
	LastSessionSettings->Set(SETTING_MAPNAME, MapName, EOnlineDataAdvertisementType::ViaOnlineService);

	CreateSessionCompleteDelegateHandle = sessionInterface->AddOnCreateSessionCompleteDelegate_Handle(CreateSessionCompleteDelegate);

	const ULocalPlayer* localPlayer = GetWorld()->GetFirstLocalPlayerFromController();
	if (!sessionInterface->CreateSession(*localPlayer->GetPreferredUniqueNetId(), NAME_GameSession, *LastSessionSettings))
	{
		sessionInterface->ClearOnCreateSessionCompleteDelegate_Handle(CreateSessionCompleteDelegateHandle);

		OnCreateSessionCompleteEvent.Broadcast(false);
	}
	

}

